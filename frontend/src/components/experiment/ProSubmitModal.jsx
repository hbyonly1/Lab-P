const GLOBAL_IMAGE_POOL_SLOT_ID = 'GLOBAL_IMAGE_POOL';
const GENERATED_IMAGE_PURPOSES = new Set(['computed', 'computed_asset', 'generated', 'auto_generated']);

function getComputedAssetSlotIds(experiment) {
  const assets = experiment?.computedAssets || experiment?.config?.computedAssets || {};
  return new Set(
    Object.values(assets)
      .map((asset) => asset?.imageSlotId)
      .filter(Boolean)
      .map(String)
  );
}

function getRequiredUploadImageSlotIds(experiment) {
  const computedAssetSlotIds = getComputedAssetSlotIds(experiment);
  return (experiment?.inputs?.images || [])
    .filter((slot) => {
      const slotId = String(slot?.id || '');
      if (!slotId) return false;
      if (computedAssetSlotIds.has(slotId)) return false;
      if (slot?.autoGenerated === true) return false;
      const purpose = String(slot?.purpose || '').toLowerCase();
      return !GENERATED_IMAGE_PURPOSES.has(purpose);
    })
    .map((slot) => String(slot.id));
}

function areRequiredImageSlotsFilled(experiment, imageSlots) {
  const requiredSlotIds = getRequiredUploadImageSlotIds(experiment);
  return requiredSlotIds.length > 0 && requiredSlotIds.every((slotId) => (
    (imageSlots?.[slotId] || []).some((file) => file?.url)
  ));
}

/**
 * 一键提交 (Pro) 专属复核弹窗
 * @param {boolean} open - 弹窗是否可见
 * @param {Array} experiments - 待提交的实验配置数组
 * @param {Function} onCancel - 取消回调
 * @param {Function} onSubmit - 确认提交回调
 */
export function ProSubmitModal({ open, experiments: propExperiments, onCancel, onSubmit, fixedTargetStudent = '', instanceKey = '' }) {
  const [experiments, setExperiments] = useState(propExperiments || []);
  const [activeExperimentId, setActiveExperimentId] = useState(null);
  const [batchImageSlots, setBatchImageSlots] = useState({});
  const [step, setStep] = useState(1);
  const [isSubmitting, setIsSubmitting] = useState(false);
  const [isPaywallOpen, setIsPaywallOpen] = useState(false);
  const [readyCount, setReadyCount] = useState(1);
  const [targetStudent, setTargetStudent] = useState('');
  const [fusedUploadEnabled, setFusedUploadEnabled] = useState(false);
  const [fusedUploadPhase, setFusedUploadPhase] = useState('manual');
  const [globalImageSlots, setGlobalImageSlots] = useState({});
  const [isAutoMatching, setIsAutoMatching] = useState(false);
  const [autoMatchSummary, setAutoMatchSummary] = useState(null);
  const [fusedAutoConfirmEnabled, setFusedAutoConfirmEnabled] = useState(true);
  const activeInstanceKeyRef = React.useRef(instanceKey);

  const userRole = getAdminUserRole();
  const resolvedTargetStudent = fixedTargetStudent || targetStudent;
  const showAdminHandoffInput = ['admin', 'reviewer'].includes(userRole) && !fixedTargetStudent;
  const localAsyncRunner = useAsyncTaskRunner({ maxVisibleJobs: 3 });
  const workspaceAsyncRunner = useWorkspaceAsyncTaskRunner();
  const {
    jobs: autoMatchJobs,
    clearFinishedJobs,
    dismissJob,
    runCeleryTask,
  } = workspaceAsyncRunner || localAsyncRunner;

  const targetExperimentIds = React.useMemo(() => (
    (propExperiments || [])
      .map((exp) => exp?.id || exp?.meta?.id)
      .filter(Boolean)
  ), [propExperiments]);
  const modalInstanceKey = React.useMemo(() => (
    instanceKey || `${resolvedTargetStudent || 'self'}:${targetExperimentIds.join(',') || 'all'}`
  ), [instanceKey, resolvedTargetStudent, targetExperimentIds]);

  useEffect(() => {
    activeInstanceKeyRef.current = modalInstanceKey;
  }, [modalInstanceKey]);

  // 每次打开弹窗时，重置状态并同步传入的实验数据
  useEffect(() => {
    if (open) {
      const nextExperiments = propExperiments || [];
      setStep(1);
      setExperiments(nextExperiments);
      setActiveExperimentId(nextExperiments[0]?.id || null);
      setBatchImageSlots({});
      setIsSubmitting(false);
      setIsPaywallOpen(false);
      setTargetStudent(fixedTargetStudent || '');
      setGlobalImageSlots({});
      setIsAutoMatching(false);
      setAutoMatchSummary(null);
      setFusedUploadEnabled(false);
      setFusedUploadPhase('manual');
      getSchoolSyncSettings()
        .then((settings) => {
          const enabled = settings?.oneClickFusedImageUploadAiEnabled === true;
          setFusedAutoConfirmEnabled(settings?.oneClickFusedImageAutoConfirmEnabled !== false);
          setFusedUploadEnabled(enabled);
          setFusedUploadPhase(enabled ? 'global' : 'manual');
        })
        .catch(() => {
          setFusedUploadEnabled(false);
          setFusedAutoConfirmEnabled(true);
          setFusedUploadPhase('manual');
        });
    }
  }, [fixedTargetStudent, modalInstanceKey, open, propExperiments]);

  const globalImages = globalImageSlots[GLOBAL_IMAGE_POOL_SLOT_ID] || [];

  // 处理图片真实上传
  const handleImageUpload = async (expId, slotId, file) => {
    const uid = file.uid || `${file.name}-${Date.now()}`;
    message.loading({ content: '正在上传...', key: `upload-${uid}` });
    try {
      const res = await uploadFile(file);
      setBatchImageSlots(prev => {
        const expSlots = prev[expId] || {};
        const slotFiles = expSlots[slotId] || [];
        return {
          ...prev,
          [expId]: {
            ...expSlots,
            [slotId]: [
              ...slotFiles,
              {
                uid: uid,
                name: file.name,
                url: res.url, // Real URL from server
                originFileObj: file
              }
            ]
          }
        };
      });
      message.success({ content: '上传成功', key: `upload-${uid}` });
    } catch (e) {
      message.error({ content: '上传失败', key: `upload-${uid}` });
    }
    return false; // 阻止默认自动上传动作
  };

  const handleImageReplace = async (expId, slotId, uidToReplace, file) => {
    message.loading({ content: '正在旋转并上传...', key: `rotate-${uidToReplace}` });
    try {
      const res = await uploadFile(file);
      setBatchImageSlots(prev => {
        const expSlots = prev[expId] || {};
        const slotFiles = expSlots[slotId] || [];
        return {
          ...prev,
          [expId]: {
            ...expSlots,
            [slotId]: slotFiles.map(item => (
              item.uid === uidToReplace
                ? { ...item, name: file.name, url: res.url, originFileObj: file }
                : item
            )),
          },
        };
      });
      message.success({ content: '图片已旋转并保存', key: `rotate-${uidToReplace}` });
      return true;
    } catch (e) {
      message.error({ content: '旋转失败', key: `rotate-${uidToReplace}` });
      return false;
    }
  };

  const handleGlobalImageUpload = async (_slotId, file) => {
    const uid = file.uid || `${file.name}-${Date.now()}`;
    message.loading({ content: '正在上传...', key: `upload-${uid}` });
    try {
      const res = await uploadFile(file);
      setGlobalImageSlots(prev => {
        const files = prev[GLOBAL_IMAGE_POOL_SLOT_ID] || [];
        return {
          ...prev,
          [GLOBAL_IMAGE_POOL_SLOT_ID]: [
            ...files,
            {
              uid,
              name: file.name,
              url: res.url,
              originFileObj: file,
              sourceIndex: files.length + 1,
            }
          ]
        };
      });
      message.success({ content: '上传成功', key: `upload-${uid}` });
    } catch (e) {
      message.error({ content: '上传失败', key: `upload-${uid}` });
    }
    return false;
  };

  const handleGlobalImageReplace = async (_slotId, uidToReplace, file) => {
    message.loading({ content: '正在旋转并上传...', key: `rotate-${uidToReplace}` });
    try {
      const res = await uploadFile(file);
      setGlobalImageSlots(prev => {
        const files = prev[GLOBAL_IMAGE_POOL_SLOT_ID] || [];
        return {
          ...prev,
          [GLOBAL_IMAGE_POOL_SLOT_ID]: files.map(item => (
            item.uid === uidToReplace
              ? { ...item, name: file.name, url: res.url, originFileObj: file }
              : item
          )),
        };
      });
      message.success({ content: '图片已旋转并保存', key: `rotate-${uidToReplace}` });
      return true;
    } catch (e) {
      message.error({ content: '旋转失败', key: `rotate-${uidToReplace}` });
      return false;
    }
  };

  const handleRemoveGlobalImage = (_slotId, uidToRemove) => {
    setGlobalImageSlots(prev => ({
      ...prev,
      [GLOBAL_IMAGE_POOL_SLOT_ID]: (prev[GLOBAL_IMAGE_POOL_SLOT_ID] || []).filter(f => f.uid !== uidToRemove),
    }));
  };

  // 处理图片移除
  const handleRemoveImage = (expId, slotId, uidToRemove) => {
    setBatchImageSlots(prev => {
      const expSlots = prev[expId] || {};
      const slotFiles = expSlots[slotId] || [];
      return {
        ...prev,
        [expId]: {
          ...expSlots,
          [slotId]: slotFiles.filter(f => f.uid !== uidToRemove)
        }
      };
    });
  };

  // 获取某个实验上传的图片总数
  const getExperimentImageCount = (expId) => {
    const expSlots = batchImageSlots[expId] || {};
    return Object.values(expSlots).reduce((total, files) => total + files.length, 0);
  };

  const imageSlotsForCheckout = (expId) => {
    const expSlots = batchImageSlots[expId] || {};
    return Object.fromEntries(
      Object.entries(expSlots)
        .map(([slotId, files]) => [
          slotId,
          (files || [])
            .filter((img) => img?.url)
            .map((img, index) => ({
              uid: img.uid || `${slotId}-${index + 1}`,
              name: img.name || `图片 ${index + 1}`,
              url: img.url,
              sourceIndex: img.sourceIndex,
            })),
        ])
        .filter(([, files]) => files.length > 0)
    );
  };

  const checkoutExperiments = React.useMemo(() => experiments
    .map((exp) => {
      const expSlots = batchImageSlots[exp.id] || {};
      const imagePaths = Object.values(expSlots).flat().map((img) => img?.url).filter(Boolean);
      return { experiment_id: exp.id, image_paths: imagePaths, image_slots: imageSlotsForCheckout(exp.id) };
    })
    .filter((item) => item.experiment_id && item.image_paths.length > 0), [experiments, batchImageSlots]);

  const activeExperimentIndex = Math.max(0, experiments.findIndex(exp => exp.id === activeExperimentId));
  const activeExperiment = experiments[activeExperimentIndex] || experiments[0] || null;

  const handleNextStep = () => {
    setStep(2);
  };

  const buildAutoMatchAssignments = (result, runContext = null) => {
    const contextExperiments = runContext?.experiments || propExperiments || [];
    const contextGlobalImages = runContext?.globalImages || globalImages;
    const byExperimentId = new Map(contextExperiments.map((exp) => [exp.id || exp.meta?.id, exp]));
    const slotMap = result?.candidate_map?.slots || {};
    const nextBatchSlots = {};
    const matchedExperimentIds = [];

    (result.matches || []).forEach((match) => {
      const sourceImage = contextGlobalImages[(Number(match.imageIndex) || 1) - 1];
      const mappedSlot = slotMap[match.slotCandidateId];
      if (!sourceImage || !mappedSlot?.experiment_id || !mappedSlot?.slot_id) return;
      if (!matchedExperimentIds.includes(mappedSlot.experiment_id)) {
        matchedExperimentIds.push(mappedSlot.experiment_id);
      }
      const expSlots = nextBatchSlots[mappedSlot.experiment_id] || {};
      const slotFiles = expSlots[mappedSlot.slot_id] || [];
      nextBatchSlots[mappedSlot.experiment_id] = {
        ...expSlots,
        [mappedSlot.slot_id]: [
          ...slotFiles,
          {
            ...sourceImage,
            uid: sourceImage.uid || `matched-${match.imageIndex}`,
            sourceIndex: match.imageIndex,
          },
        ],
      };
    });

    const matchedExperiments = matchedExperimentIds
      .map((experimentId) => byExperimentId.get(experimentId))
      .filter(Boolean)
      .map((exp) => (exp?.id ? exp : { ...exp, id: exp?.meta?.id, name: exp?.meta?.name }))
      .filter(Boolean);
    return {
      matchedExperiments,
      nextBatchSlots,
      summary: {
        matched: result.matches?.length || 0,
        unmatched: result.unmatched?.length || 0,
      },
    };
  };

  const applyAutoMatchResult = (result) => {
    const { matchedExperiments, nextBatchSlots, summary } = buildAutoMatchAssignments(result);
    if (!matchedExperiments.length) {
      setAutoMatchSummary({
        matched: 0,
        unmatched: (result.unmatched || []).length || globalImages.length,
      });
      message.warning('AI 没有匹配出可提交的实验图片槽，请检查图片或改用普通分实验上传。');
      return;
    }

    setExperiments(matchedExperiments);
    setActiveExperimentId(matchedExperiments[0]?.id || null);
    setBatchImageSlots(nextBatchSlots);
    setAutoMatchSummary(summary);
    setFusedUploadPhase('assigned');
    message.success(`预匹配完成：已匹配 ${summary.matched} 张图片`);
  };

  const submitAutoMatchResult = async (result, runContext = null) => {
    const { matchedExperiments, nextBatchSlots, summary } = buildAutoMatchAssignments(result, runContext);
    if (!matchedExperiments.length) {
      message.warning('AI 没有匹配出可提交的实验图片槽，任务未提交。');
      return;
    }
    const contextGlobalImages = runContext?.globalImages || globalImages;
    const allGlobalImageUrls = contextGlobalImages.map((img) => img?.url).filter(Boolean);
    const imagePathsOverride = Object.fromEntries(
      matchedExperiments.map((exp) => [exp.id, allGlobalImageUrls])
    );
    const imageAssignmentConfirmed = Object.fromEntries(
      matchedExperiments.map((exp) => [
        exp.id,
        areRequiredImageSlotsFilled(exp, nextBatchSlots[exp.id] || {}),
      ])
    );
    const readyExperimentCount = Object.values(imageAssignmentConfirmed).filter(Boolean).length;
    const pendingExperimentCount = matchedExperiments.length - readyExperimentCount;
    try {
      let submitResult = null;
      if (onSubmit) {
        submitResult = await onSubmit(
          nextBatchSlots,
          runContext?.targetStudent || resolvedTargetStudent,
          false,
          'pay_per_use',
          matchedExperiments,
          {
            imagePathsOverride,
            imageAssignmentConfirmed,
            modalInstanceKey: runContext?.modalInstanceKey || modalInstanceKey,
            backgroundAutoMatch: true,
          },
        );
      }
      if (submitResult?.ignored) return;
      const targetLabel = runContext?.targetStudent ? `${runContext.targetStudent} ` : '';
      if (pendingExperimentCount > 0 || summary.unmatched > 0) {
        message.warning(`${targetLabel}图片预匹配完成：${readyExperimentCount} 个实验进入 AI 预处理，${pendingExperimentCount} 个实验进入图片待对应；未匹配 ${summary.unmatched} 张。`);
      } else {
        message.success(`${targetLabel}图片预匹配完成：${readyExperimentCount} 个实验已自动进入 AI 预处理。`);
      }
    } catch (e) {
      const msg = getApiErrorMessage(e);
      message.error(`自动提交失败: ${msg}`);
    }
  };

  const handleAutoMatchImages = async () => {
    if (!globalImages.length) {
      message.warning('请先上传需要匹配的实验图片');
      return;
    }
    const runInstanceKey = modalInstanceKey;
    const runTargetStudent = resolvedTargetStudent;
    const runId = `${Date.now()}-${Math.random().toString(36).slice(2, 8)}`;
    const runContext = {
      modalInstanceKey: runInstanceKey,
      targetStudent: runTargetStudent,
      targetExperimentIds: [...targetExperimentIds],
      experiments: [...(propExperiments || [])],
      globalImages: [...globalImages],
    };
    const images = globalImages.map((img, index) => ({
      index: index + 1,
      url: img.url,
      name: img.name,
    }));
    const targetLabel = runTargetStudent || '当前账号';
    setIsAutoMatching(true);
    if (fusedAutoConfirmEnabled) {
      onCancel();
    }
    try {
      await runCeleryTask({
        jobId: `image-auto-match-${runInstanceKey}-${targetExperimentIds.join('-') || 'all'}-${runId}`,
        title: `${targetLabel} 融合图片预匹配`,
        description: `正在为 ${targetLabel} 匹配 ${images.length} 张图片。`,
        progressProfile: ASYNC_TASK_PROGRESS_PROFILES.imageAutoMatch,
        request: () => autoMatchExperimentImagesTask(images, runContext.targetExperimentIds),
        onSuccess: (result) => {
          if (fusedAutoConfirmEnabled) {
            submitAutoMatchResult(result, runContext);
          } else {
            if (activeInstanceKeyRef.current !== runInstanceKey) return;
            applyAutoMatchResult(result);
          }
        },
        successMessage: (result) => (
          fusedAutoConfirmEnabled
            ? `${targetLabel} 预匹配完成，已匹配 ${(result.matches || []).length} 张图片，正在自动流转。`
            : `${targetLabel} 预匹配完成，已匹配 ${(result.matches || []).length} 张图片，请确认。`
        ),
        failureMessage: '融合图片预匹配失败。',
      });
    } catch (e) {
      if (activeInstanceKeyRef.current === runInstanceKey) {
        message.error(e.response?.data?.detail || e.message || 'AI 图片匹配失败');
      }
    } finally {
      if (activeInstanceKeyRef.current === runInstanceKey) {
        setIsAutoMatching(false);
      }
    }
  };

  const handlePrevStep = () => {
    setStep(1);
  };

  const handleFinalSubmit = async () => {
    // 过滤出真正需要提交的实验
    const count = experiments.filter(e => getExperimentImageCount(e.id) > 0).length;
    if (count === 0) {
      message.warning('请至少上传一个实验的图片');
      return;
    }

      setIsSubmitting(true);
    try {
      if (onSubmit) {
        const result = await onSubmit(
          batchImageSlots,
          resolvedTargetStudent,
          false,
          'pay_per_use',
          experiments,
          { modalInstanceKey },
        );
        if (result?.ignored) return;
      }
      message.success('提交成功！任务已进入人工审核队列。');
      onCancel();
    } catch (e) {
      console.error(e);
      if (e.response?.status === 403 || e.status === 403) {
        setReadyCount(count);
        setIsPaywallOpen(true);
        return;
      }
      const msg = getApiErrorMessage(e);
      message.error(`提交失败: ${msg}`);
    } finally {
      setIsSubmitting(false);
    }
  };

  const handlePaywallClose = async (isHungup, planName) => {
    setIsPaywallOpen(false);
    if (isHungup === true) {
      setIsSubmitting(true);
      try {
        if (onSubmit) {
          await onSubmit(batchImageSlots, resolvedTargetStudent, true, planName, experiments);
        }
        setTimeout(() => {
          onCancel(); // 只有真正挂起订单时，才关闭底层的上传弹窗
          Modal.info({
            title: '订单已挂起',
            content: '您的任务订单已提交并处于待付款挂起状态。请确保已扫码支付，管理员核实后，您的实验将自动进入处理队列。如有疑问，请联系管理员，QQ:1952096193',
            okText: '知道了'
          });
        }, 10);
      } catch (e) {
        console.error(e);
        const msg = getApiErrorMessage(e);
        message.error(`提交失败: ${msg}`);
      } finally {
        setIsSubmitting(false);
      }
    }
  };

  // ================= 渲染视图 =================

  const renderFusedUpload = () => (
    <div className="pro-submit-step1">
      <div className="pro-submit-fused-shell">
        <Alert
          className="pro-submit-fused-alert"
          type="info"
          showIcon
          message="融合图片上传模式已开启"
          description="上传所有实验图片，模型将尝试为你自动匹配对应的实验与图片槽。"
        />
        <div className="pro-submit-active-panel">
          <ExperimentImageUploader
            images={[{ id: GLOBAL_IMAGE_POOL_SLOT_ID, label: '全部实验图片' }]}
            imageSlots={globalImageSlots}
            onImageUpload={handleGlobalImageUpload}
            onImageReplace={handleGlobalImageReplace}
            onRemoveImage={handleRemoveGlobalImage}
            title="全部实验图片上传"
            emptyTitle="拖动所有实验图片到这里"
            recognitionDef={null}
          />
        </div>
      </div>
    </div>
  );

  const renderStep1 = () => (
    <div className="pro-submit-step1">
      {fusedUploadEnabled && fusedUploadPhase === 'assigned' && autoMatchSummary && (
        <Alert
          type={autoMatchSummary.unmatched > 0 ? 'warning' : 'success'}
          showIcon
          message={`AI 预匹配完成：已匹配 ${autoMatchSummary.matched} 张，未匹配 ${autoMatchSummary.unmatched} 张`}
          style={{ marginBottom: 10 }}
        />
      )}
      {experiments.length > 0 ? (
        <div className="pro-submit-upload-layout">
          <div className="pro-submit-experiment-list">
            {experiments.map((exp, index) => {
              const imageCount = getExperimentImageCount(exp.id);
              const isActive = activeExperiment?.id === exp.id;
              return (
                <button
                  key={exp.id}
                  type="button"
                  className={`pro-submit-experiment-item ${isActive ? 'is-active' : ''}`}
                  onClick={() => setActiveExperimentId(exp.id)}
                >
                  <span className="pro-submit-experiment-index">{index + 1}</span>
                  <span className="pro-submit-experiment-main">
                    <strong>{exp.name}</strong>
                    <span className={imageCount > 0 ? 'is-ready' : ''}>
                      {imageCount > 0 ? `已上传 ${imageCount} 张` : '未上传'}
                    </span>
                  </span>
                </button>
              );
            })}
          </div>

          <div className="pro-submit-active-panel">
            <div className="pro-submit-active-head">
              <h3>{activeExperiment?.name} - 签字原始数据上传</h3>
              {fusedUploadEnabled && (
                <Button size="small" onClick={() => setFusedUploadPhase('global')}>
                  返回融合上传
                </Button>
              )}
            </div>
            <ExperimentImageUploader
              images={activeExperiment?.inputs?.images || []}
              imageSlots={batchImageSlots[activeExperiment?.id] || {}}
              onImageUpload={(slotId, file) => handleImageUpload(activeExperiment.id, slotId, file)}
              onImageReplace={(slotId, uid, file) => handleImageReplace(activeExperiment.id, slotId, uid, file)}
              onRemoveImage={(slotId, uid) => handleRemoveImage(activeExperiment.id, slotId, uid)}
              recognitionDef={null} // 屏蔽 AI 一键识别按钮，弹窗内仅做上传
            />
          </div>
        </div>
      ) : (
        <div style={{ color: '#999', textAlign: 'center', padding: '20px' }}>暂无待提交的实验</div>
      )}
    </div>
  );

  const renderStep2 = () => {
    return (
      <div className="pro-submit-step2">
        <div className="pro-submit-confirm-head">
          <h3>即将提交</h3>
        </div>

        <div className="pro-submit-confirm-list-shell">
          <ul className="pro-submit-confirm-list">
            {experiments.map((exp) => {
              const count = getExperimentImageCount(exp.id);
              const isSubmittingExp = count > 0;
              return (
                <li key={exp.id} className="pro-submit-confirm-row">
                  <span className="pro-submit-confirm-name">{exp.name}</span>
                  <span className={`pro-submit-confirm-pill ${isSubmittingExp ? 'is-ready' : 'is-empty'}`}>
                    {isSubmittingExp ? `${count} 张图片` : '不提交 (留空)'}
                  </span>
                </li>
              );
            })}
          </ul>
        </div>

        <div className="pro-submit-confirm-bottom">
          {showAdminHandoffInput && (
            <div className="pro-submit-admin-handoff">
              <div>管理员代交设置 (可选)</div>
              <Input
                placeholder="请输入代交的目标学号，留空则绑定在当前账号下"
                value={targetStudent}
                onChange={(e) => setTargetStudent(e.target.value)}
              />
            </div>
          )}

          <div className="pro-submit-confirm-warning">
            <strong>警告：</strong> 选择提交的实验将会覆写系统中已存在的数据，进入后台人工复核流程并正式提交，正式提交后你无需任何操作！此操作不可逆，请再三确认！
          </div>
        </div>
      </div>
    );
  };

  return (
    <Modal
      rootClassName="pro-submit-modal-root"
      className={`pro-submit-fullscreen-modal ${step === 1 ? 'is-upload-step' : 'is-confirm-step'}`}
      open={open}
      title={
        <div className="pro-submit-modal-title">
          <span>
            <CrownOutlined /> 一键批量提交 - 上传数据
          </span>
          <p>请上传实验数据图片，无需担心任何事情，我们会接管后续所有流程。</p>
        </div>
      }
      onCancel={!isSubmitting ? onCancel : undefined}
      closable={!isSubmitting}
      maskClosable={!isSubmitting}
      width="100vw"
      footer={
        step === 1 ? (
          <div style={{ display: 'flex', justifyContent: 'flex-end', gap: '12px' }}>
            <Button onClick={onCancel}>取消</Button>
            {fusedUploadEnabled && fusedUploadPhase === 'global' ? (
              <Button
                className="recognize-primary-button"
                type="primary"
                onClick={handleAutoMatchImages}
                loading={isAutoMatching}
                disabled={globalImages.length === 0}
              >
                自动匹配图片
              </Button>
            ) : (
              <Button
                className="recognize-primary-button"
                type="primary"
                onClick={handleNextStep}
                disabled={experiments.length === 0}
              >
                下一步，确认清单
              </Button>
            )}
          </div>
        ) : (
          <div style={{ display: 'flex', justifyContent: 'flex-end', gap: '12px' }}>
            <Button onClick={handlePrevStep} disabled={isSubmitting}>返回修改</Button>
            <Button
              className="recognize-primary-button"
              type="primary"
              danger
              onClick={handleFinalSubmit}
              loading={isSubmitting}
            >
              确认正式提交
            </Button>
          </div>
        )
      }
    >
      {step === 1 ? (
        fusedUploadEnabled && fusedUploadPhase === 'global' ? renderFusedUpload() : renderStep1()
      ) : renderStep2()}

      <PaywallModal
        open={isPaywallOpen}
        onCancel={handlePaywallClose}
        taskCount={readyCount}
        experiments={checkoutExperiments}
      />
      {!workspaceAsyncRunner && (
        <AsyncJobFloatingPanel
          jobs={autoMatchJobs}
          onDismiss={dismissJob}
          onClearDone={clearFinishedJobs}
          onRetry={(job) => {
            if (job.id.startsWith('image-auto-match-')) handleAutoMatchImages();
          }}
        />
      )}
    </Modal>
  );
}
